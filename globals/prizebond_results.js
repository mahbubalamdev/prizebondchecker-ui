export var search_prizebond = (query) => {
  query = conver_to_english(query);
  let search_result = [];
  let search_terms = query.split(",").map(item => item.trim())
  for (let search_term of search_terms) {
    for (let result of results) {
      if (result.first.bonds.includes(search_term)) {
        search_result.push({
          date: result.date,
          draw_number: result.draw_number,
          prize: "প্রথম পুরস্কারঃ",
          amount: result.first.amount,
          bond: search_term,
        });
      }
      if (result.second.bonds.includes(search_term)) {
        search_result.push({
          date: result.date,
          draw_number: result.draw_number,
          prize: "দ্বিতীয় পুরস্কারঃ",
          amount: result.second.amount,
          bond: search_term,
        });
      }
      if (result.third.bonds.includes(search_term)) {
        search_result.push({
          date: result.date,
          draw_number: result.draw_number,
          prize: "তৃতীয় পুরস্কারঃ",
          amount: result.third.amount,
          bond: search_term,
        });
      }
      if (result.fourth.bonds.includes(search_term)) {
        search_result.push({
          date: result.date,
          draw_number: result.draw_number,
          prize: "চতুর্থ পুরস্কারঃ",
          amount: result.fourth.amount,
          bond: search_term,
        });
      }
      if (result.fifth.bonds.includes(search_term)) {
        search_result.push({
          date: result.date,
          draw_number: result.draw_number,
          prize: "পঞ্চম পুরস্কারঃ",
          amount: result.fifth.amount,
          bond: search_term,
        });
      }
    }
    
  }
  return search_result;
};
